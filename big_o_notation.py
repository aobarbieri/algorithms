# Big O Notation - how fast an algorightm is telling you the number of operations an algorithm will make.

# O = Big O
# n = number of operations

#  O(n)
# LINEAR TIME - the time taken to complete a task or reach a certain point is directly proportional to the size or complexity of the task. This means that if the size of the task doubles, the time taken to complete it also doubles.

#  O(log n)
# LOGARITHMIC TIME (or log time) - Time taken increases slowly as the input size grows. Highly efficient, often seen in binary search and tree-based data structures.